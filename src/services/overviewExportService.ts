async function fetchAllLaunchPages(
  input: OverviewLaunchesExportInput,
): Promise<{ launches: OverviewLaunchApiRow[]; truncated: boolean; total: number }> {
  const collected: OverviewLaunchApiRow[] = [];
  let truncated = false;
  let total = 0;
  for (let page = 1; page <= MAX_LAUNCH_PAGES; page++) {
    const res = await input.fetchAllLaunches({
      ...input.fetchParamsBase,
      page,
      perPage: LAUNCH_EXPORT_PER_PAGE,
    });
    collected.push(...res.launches);
    total = res.meta.total;
    if (page >= res.meta.lastPage) return { launches: collected, truncated: false, total };
    if (page === MAX_LAUNCH_PAGES && page < res.meta.lastPage) truncated = true;
  }
  return { launches: collected, truncated, total };
}

const LAUNCH_CSV_COLUMNS: Array<{ key: keyof OverviewLaunchApiRow | 'startTime'; label: string }> = [
  { key: 'testRunExecutionId', label: 'ID' },
  { key: 'displayName', label: 'Name' },
  { key: 'startTime', label: 'Start' },
  { key: 'runnedByLabel', label: 'Ran by' },
  { key: 'source', label: 'Source' },
  { key: 'durationLabel', label: 'Duration' },
  { key: 'total', label: 'Total' },
  { key: 'passed', label: 'Passed' },
  { key: 'failed', label: 'Failed' },
  { key: 'skipped', label: 'Skipped' },
  { key: 'productBug', label: 'Product bug' },
  { key: 'autoBug', label: 'Auto bug' },
  { key: 'systemIssue', label: 'System issue' },
  { key: 'toInvestigate', label: 'To investigate' },
];

function launchCell(row: OverviewLaunchApiRow, key: (typeof LAUNCH_CSV_COLUMNS)[number]['key']): string | number {
  if (key === 'startTime') {
    return row.startTimeRaw ?? row.startTimeDisplay ?? row.startTimeRelative ?? '';
  }
  const value = row[key as keyof OverviewLaunchApiRow];
  if (value == null) return '';
  return typeof value === 'number' ? value : String(value);
}

async function exportLaunchesCsv(input: OverviewLaunchesExportInput): Promise<void> {
  const { launches, truncated, total } = await fetchAllLaunchPages(input);
  if (launches.length === 0) {
    throw new Error('No launches match the current filters.');
  }
  const rows: (string | number)[][] = [];
  rows.push(['Overview – Launches']);
  rows.push(['Generated', new Date().toISOString()]);
  rows.push(['Rows exported', launches.length]);
  rows.push(['Total matching launches', total]);
  if (truncated) rows.push(['Note', `Export capped at ${launches.length} rows.`]);
  for (const line of filterSummaryLines(input)) rows.push([line]);
  rows.push([]);
  rows.push(LAUNCH_CSV_COLUMNS.map(c => c.label));
  for (const row of launches) {
    rows.push(LAUNCH_CSV_COLUMNS.map(c => launchCell(row, c.key)));
  }
  downloadBlob(new Blob([toCsv(rows)], { type: 'text/csv;charset=utf-8;' }), `overview-launches-${todayIsoDate()}.csv`);
}

export async function exportOverviewLaunches(input: OverviewLaunchesExportInput): Promise<void> {
  if (input.format === 'pdf') {
    await screenshotTabToPdf(input, 'Overview', 'Launches snapshot', 'overview-launches');
    return;
  }
  await exportLaunchesCsv(input);
}

