function exportWidgetsCsv(input: OverviewWidgetsExportInput): void {
  const { data } = input;
  const rows: (string | number)[][] = [];
  rows.push(['Overview – Widgets']);
  rows.push(['Window from', data.window.from]);
  rows.push(['Window to', data.window.to]);
  rows.push(['Window days', data.window.days]);
  rows.push(['Generated', new Date().toISOString()]);
  for (const line of filterSummaryLines(input)) rows.push([line]);
  rows.push([]);
  rows.push(['=== WEEKLY TOTALS ===']);
  rows.push(['Pass', 'Fail', 'Pass rate %']);
  rows.push([
    data.weeklyTotals.pass,
    data.weeklyTotals.fail,
    data.weeklyTotals.passRate != null ? data.weeklyTotals.passRate.toFixed(2) : '',
  ]);
  rows.push([]);
  rows.push(['=== DEFECT MIX ===']);
  rows.push(['Tag', 'Label', 'Fail count', 'Percent %']);
  for (const m of data.defectMix) {
    rows.push([m.tag, m.label, m.failCount, m.percent != null ? m.percent.toFixed(2) : '']);
  }
  rows.push([]);
  rows.push(['=== EXECUTION BY SERVICE ===']);
  rows.push(['Key', 'Label', 'Pass', 'Fail', 'Pass rate %', 'Band']);
  for (const r of data.executionByService) {
    rows.push([r.key, r.label, r.pass, r.fail, r.passRate != null ? r.passRate.toFixed(2) : '', r.band]);
  }
  rows.push([]);
  rows.push(['=== EXECUTION BY COUNTRY ===']);
  rows.push(['Key', 'Label', 'Pass', 'Fail', 'Pass rate %', 'Band']);
  for (const r of data.executionByCountry) {
    rows.push([r.key, r.label, r.pass, r.fail, r.passRate != null ? r.passRate.toFixed(2) : '', r.band]);
  }
  downloadBlob(new Blob([toCsv(rows)], { type: 'text/csv;charset=utf-8;' }), `overview-widgets-${todayIsoDate()}.csv`);
}

function formatWindowDate(iso: string | undefined): string {
  if (!iso) return '';
  const d = new Date(iso);
  if (Number.isNaN(d.getTime())) return iso;
  return d.toLocaleDateString(undefined, { year: 'numeric', month: 'short', day: 'numeric' });
}

export async function exportOverviewWidgets(input: OverviewWidgetsExportInput): Promise<void> {
  if (input.format === 'pdf') {
    const from = formatWindowDate(input.data.window.from);
    const to = formatWindowDate(input.data.window.to);
    const days = input.data.window.days;
    const subtitle = `Window ${from} to ${to} (${days} days)`;
    await screenshotTabToPdf(input, 'Overview', subtitle, 'overview-widgets');
    return;
  }
  exportWidgetsCsv(input);
}

